/**
 * Firestore Timestamp type placeholder
 * Replace with actual Timestamp when Firebase is installed
 */
type Timestamp = {
  seconds: number;
  nanoseconds: number;
  toDate: () => Date;
};

/**
 * Convert Date to Firestore Timestamp and vice versa
 */
export type FirestoreDate = Date | Timestamp;

/**
 * Base document interface with common Firestore fields
 */
export interface FirestoreDocument {
  id: string;
  createdAt: FirestoreDate;
  updatedAt?: FirestoreDate;
}

