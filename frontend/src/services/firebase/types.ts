/**
 * Utility type to convert Firestore timestamps to Dates
 */
export type ConvertTimestamps<T> = {
  [K in keyof T]: T[K] extends FirestoreDate
    ? Date
    : T[K] extends FirestoreDate | undefined
    ? Date | undefined
    : T[K] extends object
    ? ConvertTimestamps<T[K]>
    : T[K];
};
