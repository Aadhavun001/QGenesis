/**
 * Material document in Firestore
 * Stored under users/{userId}/materials/{materialId}.
 * Original file in Cloud Storage at users/{userId}/materials/{materialId}/{fileName}.
 * Full extracted text in `content` (capped to stay under 1 MiB doc limit).
 */
export interface FirestoreMaterial extends FirestoreDocument {
  fileName: string;
  fileSize: number;
  fileType: string;
  totalPages: number;
  storageUrl: string; // Cloud Storage download URL for the original file
  title: string;
  subject: string;
  department: string;
  semester?: string;
  regulation?: string;
  staffId: string;
  institution?: string;
  place?: string;
  status: 'processing' | 'ready' | 'failed';
  // Full extracted text (capped ~900k chars for Firestore 1 MiB limit)
  content?: string;
  contentTruncated?: boolean;
  // NLP global results
  globalKeywords: string[];
  globalKeyPhrases: string[];
  academicLevel: string;
  chunkCount: number;
  vocabularyRichness: number;
  wordCount: number;
  extractionMethod?: string;
  processingTimeMs?: number;
  processedAt?: FirestoreDate;
  /** Topic names for list view (also stored in topics subcollection) */
  topicNames?: string[];
  /** Identifies extraction type: "quick" (text-only, fast) or "full" (tables, images/diagrams/OCR) */
  extractionType?: 'quick' | 'full';
}

